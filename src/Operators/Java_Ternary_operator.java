public class Java_Ternary_operator {
    public static void main(String args[]){  
        int a=2;  
        int b=5;  
        int min=(a<b)?a:b;  
        System.out.println(min);  
        
        //again we try to print false conditions
         a=10;  
         b=5;  
         min=(a<b)?a:b;  
         System.out.println(min);
    }
      
  

    
}
